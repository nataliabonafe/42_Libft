#include "libft.h"

int	ft_atoi(const char *str)
{
	int	a;
	int	s;
	int	i;

	a = 0;
	s = 1;
	i = 0;
	while ((str[a] == ' ' || str[a] == '\n'
			|| str[a] == '\t' || str[a] == '\v'
			|| str[a] == '\f' || str[a] == '\r') && str[a])
		a++;
	if ((str[a] == '+' || str[a] == '-') && str[a])
	{
		if (str[a] == '-')
			s = -1;
		a++;
	}
	while (str[a] >= '0' && str[a] <= '9')
	{
		i = (str[a] - '0') + (i * 10);
		a++;
	}
	return (s * i);
}
